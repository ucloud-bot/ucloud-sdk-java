package cn.ucloud.pathx.models;


import cn.ucloud.common.annotation.NotEmpty;
import cn.ucloud.common.annotation.UCloudParam;
import cn.ucloud.common.request.Request;

public class ModifyGlobalSSHOriginInfoRequest extends Request {

    /** 项目ID,如org-xxxx。请参考[GetProjectList接口](https://docs.ucloud.cn/api/summary/get_project_list) */
    @NotEmpty
    @UCloudParam("ProjectId")
    private String projectId;

    /** Free,Basic,Enterprise,Ultimate */
    @NotEmpty
    @UCloudParam("InstanceType")
    private String instanceType;

    /** 被SSH访问的源站IP */
    @NotEmpty
    @UCloudParam("RsIP")
    private String rsIP;

    /** 资源ID:ugaa-xxxxxxx */
    @NotEmpty
    @UCloudParam("InstanceId")
    private String instanceId;

    /** 填写支持SSH访问IP的地区名称，如“洛杉矶”，“新加坡”，“香港”，“东京”，“华盛顿”，“法兰克福”。Area和AreaCode两者必填一个 */
    @UCloudParam("Area")
    private String area;

    /** AreaCode, 区域航空港国际通用代码。Area和AreaCode两者必填一个 */
    @UCloudParam("AreaCode")
    private String areaCode;

    public String getProjectId() {
        return projectId;
    }

    public void setProjectId(String projectId) {
        this.projectId = projectId;
    }

    public String getInstanceType() {
        return instanceType;
    }

    public void setInstanceType(String instanceType) {
        this.instanceType = instanceType;
    }

    public String getRsIP() {
        return rsIP;
    }

    public void setRsIP(String rsIP) {
        this.rsIP = rsIP;
    }

    public String getInstanceId() {
        return instanceId;
    }

    public void setInstanceId(String instanceId) {
        this.instanceId = instanceId;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getAreaCode() {
        return areaCode;
    }

    public void setAreaCode(String areaCode) {
        this.areaCode = areaCode;
    }
}
